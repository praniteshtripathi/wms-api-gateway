package com.wms.pojo;
public class ExcelOrder{
	 private String deadLine;
	 private String name;
	 private String intendedVehicle;
	 private String target;
	 private String orderUploadStatus;
	 
	public String getDeadLine() {
		return deadLine;
	}
	public void setDeadLine(String deadLine) {
		this.deadLine = deadLine;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntendedVehicle() {
		return intendedVehicle;
	}
	public void setIntendedVehicle(String intendedVehicle) {
		this.intendedVehicle = intendedVehicle;
	}
	public String getTarget() {
		return target;
	}
	public void setTarget(String target) {
		this.target = target;
	}
	public String getOrderUploadStatus() {
		return orderUploadStatus;
	}
	public void setOrderUploadStatus(String orderUploadStatus) {
		this.orderUploadStatus = orderUploadStatus;
	}
	
	
}
