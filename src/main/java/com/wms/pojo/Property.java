package com.wms.pojo;

public class Property {
	 private String key;
	 private String value;


	 // Getter Methods 

	 public String getKey() {
	  return key;
	 }

	 public String getValue() {
	  return value;
	 }

	 // Setter Methods 

	 public void setKey(String key) {
	  this.key = key;
	 }

	 public void setValue(String value) {
	  this.value = value;
	 }
}